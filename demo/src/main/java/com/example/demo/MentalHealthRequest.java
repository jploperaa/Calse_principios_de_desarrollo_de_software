package com.example.demo;

public class MentalHealthRequest {
    private String nombre;
    private String posicion;
    private int animo;
    private int estres;
    private int sueno;
    private int apetito;
    private int motivacion;
    private int interaccionSocial;

    // Getters and Setters
    public String getNombre() { return nombre; }
    public void setNombre(String nombre) { this.nombre = nombre; }
    public String getPosicion() { return posicion; }
    public void setPosicion(String posicion) { this.posicion = posicion; }
    public int getAnimo() { return animo; }
    public void setAnimo(int animo) { this.animo = animo; }
    public int getEstres() { return estres; }
    public void setEstres(int estres) { this.estres = estres; }
    public int getSueno() { return sueno; }
    public void setSueno(int sueno) { this.sueno = sueno; }
    public int getApetito() { return apetito; }
    public void setApetito(int apetito) { this.apetito = apetito; }
    public int getMotivacion() { return motivacion; }
    public void setMotivacion(int motivacion) { this.motivacion = motivacion; }
    public int getInteraccionSocial() { return interaccionSocial; }
    public void setInteraccionSocial(int interaccionSocial) { this.interaccionSocial = interaccionSocial; }
}
